// ── Helper: fill all rows of a DynamicTable ───────────────────────────────
/**
 * Fills every editable row in the first table on the page.
 * DynamicTable renders: tr per criterion, with input (score) + textarea (notes).
 * For assessor steps: column 0 = no_urut (read-only label), column 1 = aspek (label),
 * column 2 = skor (number input), column 3 = catatan (optional textarea).
 */
async function fillAllTableRows(
  page: Page,
  score: number,
  notes: string = '',
): Promise<void> {
  await page.waitForSelector('table tbody tr', { timeout: 10_000 });
  const rows = await page.locator('table tbody tr').all();

  for (const row of rows) {
    // Number inputs (score column)
    const numberInputs = await row.locator('input[type="number"], input:not([type="file"])').all();
    for (const input of numberInputs) {
      await input.waitFor({ state: 'visible', timeout: 5_000 }).catch(() => null);
      await input.clear();
      await input.fill(score.toString());
      await page.waitForTimeout(80);
    }

    // Textarea (catatan/notes column — optional, only when notes provided)
    if (notes) {
      const textareas = await row.locator('textarea').all();
      for (const ta of textareas) {
        await ta.waitFor({ state: 'visible', timeout: 2_000 }).catch(() => null);
        await ta.clear();
        await ta.fill(notes);
        await page.waitForTimeout(80);
      }
    }
  }
}

/**
 * Navigate to a submission page and wait for the choosetask form to load.
 */
async function gotoSubmission(page: Page, taskId: string, route = SUBMISSION_ROUTE): Promise<SubmissionPage> {
  await page.goto(`${route}/${taskId}`);
  await waitForPageLoad(page);
  await page.waitForResponse(
    (r) => r.url().includes('/choosetask') || r.url().includes('/v2/choosetask'),
    { timeout: 15_000 },
  ).catch(() => null);
  await page.waitForTimeout(800); // allow React render after choosetask
  return new SubmissionPage(page);
}

/**
 * Approve a step and capture the new task_id from URL navigation.
 * Returns the new task_id, or empty string if navigation didn't happen.
 */
async function approveAndCapture(
  page: Page,
  submission: SubmissionPage,
  route = SUBMISSION_ROUTE,
): Promise<string> {
  const [apiResp] = await Promise.all([
    waitForApiResponse(page, '/responsetask'),
    submission.approveButton.click(),
  ]);

  // Wait for navigation to the next task or back to list
  await Promise.race([
    page.waitForURL(/\/submission\//, { timeout: 10_000 }).catch(() => null),
    page.waitForURL(/\/spme$/, { timeout: 10_000 }).catch(() => null),
    page.waitForURL(/\/mahadaly$/, { timeout: 10_000 }).catch(() => null),
  ]);

  const url = page.url();
  const match = url.match(/\/submission[/-]?spme\/([^/]+)$/) ?? url.match(/\/submission\/([^/]+)$/);
  return match?.[1] ?? '';
}

/**
 * Find the first pending task for a role by navigating to the assessment inbox.
 * Returns the task_id found in the first row, or empty string if none.
 */
async function findFirstPendingTask(
  page: Page,
  listRoute: string,
): Promise<string> {
  await page.goto(listRoute);
  await waitForPageLoad(page);
  await waitForTableLoad(page).catch(() => null);

  const firstRowBtn = page.locator('tbody tr').first().locator('button').first();
  const hasTask = await firstRowBtn.isVisible({ timeout: 5_000 }).catch(() => false);
  if (!hasTask) return '';

  // Capture navigation after click
  const [, newUrl] = await Promise.all([
    firstRowBtn.click(),
    page.waitForURL(/\/submission[-/]/, { timeout: 10_000 }).catch(() => ''),
  ]);

  const url = page.url();
  const match = url.match(/\/submission[/-]?spme\/([^/]+)$/) ?? url.match(/\/submission\/([^/]+)$/);
  return match?.[1] ?? '';
}

